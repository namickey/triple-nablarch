CREATE TABLE PUBLIC.USERS (
  USER_ID INTEGER NOT NULL,
  KANJI_NAME VARCHAR(128) NOT NULL,
  KANA_NAME VARCHAR(128) NOT NULL,
  PRIMARY KEY (USER_ID)
);
COMMENT ON table PUBLIC.USERS is 'ユーザ';
COMMENT ON column PUBLIC.USERS.USER_ID is 'ユーザID';
COMMENT ON column PUBLIC.USERS.KANJI_NAME is '漢字氏名';
COMMENT ON column PUBLIC.USERS.KANA_NAME is 'ふりがな';
