CREATE TABLE PUBLIC.CLIENT (
  CLIENT_ID SERIAL NOT NULL,
  CLIENT_NAME VARCHAR(128) NOT NULL,
  INDUSTRY_CODE CHAR(2) NOT NULL,
  PRIMARY KEY (CLIENT_ID)
);
COMMENT ON table PUBLIC.CLIENT is '顧客';
COMMENT ON column PUBLIC.CLIENT.CLIENT_ID is '顧客ID';
COMMENT ON column PUBLIC.CLIENT.CLIENT_NAME is '顧客名';
COMMENT ON column PUBLIC.CLIENT.INDUSTRY_CODE is '業種コード';
